public class Calculator {

    public static double add(int num1, int num2){
        return num1+num2;
    }

    public static double subtruct(int num1, int num2){
        double sum = num1+ num2; 
        return sum;
    }
    
    public static double multiply(int num1, int num2){
        return num1*num2;
    }
    
    public static double delete(int num1, int num2){
        return num1/num2;
    }
}
